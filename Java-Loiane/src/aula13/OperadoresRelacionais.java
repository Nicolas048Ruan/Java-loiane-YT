package aula13;

/* ==   igual a
   !=   diferente de
   >    maior que
   <    menor que
   >=   maior ou igual que
   <=   menor ou igual que    
 */

public class OperadoresRelacionais {
    
    public static void main (String[] args){

        int valor1 = 1;
        int valor2 = 2;

        System.out.println("valor1 == valor2: " + (valor1 == valor2));
        System.out.println("valor1 != valor2: " + (valor1 != valor2));
        System.out.println("valor1 > valor2: " + (valor1 > valor2));
        System.out.println("valor1 >= valor2: " + (valor1 >= valor2));
        System.out.println("valor1 < valor2: " + (valor1 < valor2));
        System.out.println("valor1 <= valor2: " + (valor1 <= valor2));
    }
}
